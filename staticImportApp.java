package review0407;
import static java.lang.Math.*;

public class staticImportApp {

	public static void main(String[] args) {
		//random(), round(), floor(), ceil() 테스트해보기
		System.out.println("랜덤 : "+ random());
		System.out.println("round : "+ round(3));
		System.out.println("floor : "+ floor(-3.5));
		System.out.println("ceil : "+ ceil(-51.51));
	}
}
